def iter_vcf_rows(vcf_path: Path, sample_name: str):
    """Iterate over VCF records and extract F1R2/F2R1 strand-specific allele
    counts.

    Parses a VCF file and extracts strand orientation information (F1R2 and F2R1
    counts) for each variant. These counts represent read pairs in different
    orientations and are used to infer the transcriptional strand.

    F1R2/F2R1 counts are typically generated by variant callers like GATK
    Mutect2 and indicate:
    - F1R2: Read1 forward, Read2 reverse orientation
    - F2R1: Read1 reverse, Read2 forward orientation

    Records without F1R2/F2R1 annotations are skipped. For multi-allelic sites,
    each alternate allele is yielded as a separate row.

    Args:
        vcf_path (Path): Path to the VCF file to parse.
        sample_name (str): Name of the sample in the VCF file to extract data
            from.

    Yields:
        tuple: A 9-element tuple containing:
            - chrom (str): Chromosome name
            - pos (int): Variant position (1-based)
            - vcf_id (str): VCF ID field
            - ref (str): Reference allele
            - alt (str): Alternate allele
            - f1r2_ref (int): F1R2 count for reference allele
            - f1r2_alt (int): F1R2 count for alternate allele
            - f2r1_ref (int): F2R1 count for reference allele
            - f2r1_alt (int): F2R1 count for alternate allele
    """
    idx_ref = 0
    with vcfpy.Reader.from_path(vcf_path) as reader:
        for record in reader:
            chrom = record.CHROM  # type: ignore
            pos = record.POS  # type: ignore
            vcf_id = record.ID[0]  # type: ignore
            ref = record.REF  # type: ignore
            alts = record.ALT  # type: ignore
            # f1r2 = record.calls[0].data.get("F1R2")  # type: ignore
            # f2r1 = record.calls[0].data.get("F2R1")  # type: ignore
            f1r2 = record.call_for_sample[sample_name].data.get("F1R2")  # type: ignore
            f2r1 = record.call_for_sample[sample_name].data.get("F2R1")  # type: ignore
            if f1r2 is None or f2r1 is None:
                continue
            for i, _alt in enumerate(alts):
                idx_alt = i + 1
                yield (
                    chrom,
                    pos,
                    vcf_id,
                    ref,
                    _alt.value,  # type: ignore
                    f1r2[idx_ref],
                    f1r2[idx_alt],
                    f2r1[idx_ref],
                    f2r1[idx_alt],
                )


def get_vcf_data(vcf_path, sample_name):
    """Extract VCF data and calculate strand bias metrics from F1R2/F2R1 counts.

    Processes VCF file to create a DataFrame with variant information and
    computed strand bias metrics. Calculates two key percentages:

    1. pct_f1r2: Proportion of all reads in F1R2 orientation
        = (F1R2_ref + F1R2_alt) / (F1R2_ref + F1R2_alt + F2R1_ref + F2R1_alt)

    2. pct_f1r2_alt: Proportion of alternate allele reads in F1R2 orientation
        = F1R2_alt / (F1R2_alt + F2R1_alt)

    Strand interpretation depends on library strandedness:
    - fr-firststrand (ISR): high pct_f1r2 → - strand, low → + strand
    - fr-secondstrand (ISF): high pct_f1r2 → + strand, low → - strand
    - unstranded: pct_f1r2 not used for strand inference

    Args:
        vcf_path (Path): Path to the VCF file.
        sample_name (str): Name of the sample in the VCF file.

    Returns:
        pl.DataFrame: Polars DataFrame with columns:
            - chrom, pos, id, ref, alt: Variant identifiers
            - n_f1r2_ref, n_f1r2_alt: F1R2 counts for ref/alt alleles
            - n_f2r1_ref, n_f2r1_alt: F2R1 counts for ref/alt alleles
            - pct_f1r2: Overall F1R2 percentage
            - pct_f1r2_alt: F1R2 percentage for alternate allele only
            - no_reads: Flag indicating no read coverage
            - no_reads_alt: Flag indicating no alternate allele reads
    """
    schema = {
        "chrom": pl.Utf8,
        "pos": pl.Int64,
        "id": pl.Utf8,
        "ref": pl.Utf8,
        "alt": pl.Utf8,
        "n_f1r2_ref": pl.Int64,
        "n_f1r2_alt": pl.Int64,
        "n_f2r1_ref": pl.Int64,
        "n_f2r1_alt": pl.Int64,
    }
    rows = list(iter_vcf_rows(vcf_path, sample_name))
    if not rows:
        return pl.DataFrame(
            schema={
                **schema,
                "pct_f1r2": pl.Float64,
                "pct_f1r2_alt": pl.Float64,
                "no_reads": pl.Boolean,
                "no_reads_alt": pl.Boolean,
            }
        )
    df = pl.DataFrame(rows, schema=schema, orient="row")

    df = df.with_columns(
        (
            (pl.col("n_f1r2_ref") + pl.col("n_f1r2_alt"))
            / (
                pl.col("n_f1r2_ref")
                + pl.col("n_f1r2_alt")
                + pl.col("n_f2r1_ref")
                + pl.col("n_f2r1_alt")
            )
        ).alias("pct_f1r2"),
        (pl.col("n_f1r2_alt") / (pl.col("n_f1r2_alt") + pl.col("n_f2r1_alt"))).alias(
            "pct_f1r2_alt"
        ),
    ).with_columns(
        pl.when(pl.col("pct_f1r2").is_nan())
        .then(True)
        .otherwise(False)
        .alias("no_reads"),
        pl.when(pl.col("pct_f1r2_alt").is_nan())
        .then(True)
        .otherwise(False)
        .alias("no_reads_alt"),
    )

    return df


