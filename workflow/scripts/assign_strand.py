# *--------------------------------------------------------------------------* #
# * Strandedness mapping (F1R2/F2R1 to transcriptional strand):              * #
# *   fr-firststrand (Salmon ISR): F1R2 = -, F2R1 = +                        * #
# *   fr-secondstrand (Salmon ISF): F1R2 = +, F2R1 = -                       * #
# *   unstranded: no strand inference from F1R2/F2R1                         * #
# *--------------------------------------------------------------------------* #
def _get_strand_mapping(strandedness):
    """Return (high_f1r2_strand, low_f1r2_strand) for a given strandedness.

    Args:
        strandedness (str): One of "fr-firststrand", "fr-secondstrand", or
        "unstranded".

    Returns:
        tuple or None: (strand_when_high_f1r2, strand_when_low_f1r2), or None
        for unstranded.
    """
    if strandedness == "fr-firststrand":
        return ("-", "+")
    elif strandedness == "fr-secondstrand":
        return ("+", "-")
    elif strandedness == "unstranded":
        return None
    else:
        raise ValueError(
            f"Unknown strandedness: {strandedness!r}. "
            "Expected 'fr-firststrand', 'fr-secondstrand', or 'unstranded'."
        )


def _clarify_strand(input_col, new_col, f1r2_pct_col, rate, strandedness):
    """Helper function to clarify ambiguous strand assignments using F1R2/F2R1
    bias.

    When GTF annotation doesn't provide a clear strand (not + or -), uses the
    F1R2/F2R1 ratio to infer the strand based on the library strandedness.

    For fr-firststrand (ISR):
    - pct_f1r2 >= rate: Assign - strand
    - pct_f1r2 <= (1-rate): Assign + strand

    For fr-secondstrand (ISF):
    - pct_f1r2 >= rate: Assign + strand
    - pct_f1r2 <= (1-rate): Assign - strand

    For unstranded: Keep original strand (no clarification).

    Args:
        input_col (str): Name of input column with strand information from GTF.
        new_col (str): Name of output column for clarified strand.
        f1r2_pct_col (str): Name of column with F1R2 percentage (0 to 1).
        rate (float): Threshold rate for strong strand bias (typically 0.9).
        strandedness (str): Library strandedness type.

    Returns:
        pl.Expr: Polars expression for strand clarification that can be used
            in with_columns().
    """
    mapping = _get_strand_mapping(strandedness)
    if mapping is None:
        return pl.col(input_col).alias(new_col)

    high_strand, low_strand = mapping
    return (
        pl.when(
            (~pl.col(input_col).is_in(["+", "-"])) & (pl.col(f1r2_pct_col) >= rate)
        )
        .then(pl.lit(high_strand))
        .when(
            (~pl.col(input_col).is_in(["+", "-"]))
            & (pl.col(f1r2_pct_col) <= (1 - rate))
        )
        .then(pl.lit(low_strand))
        .otherwise(pl.col(input_col))
        .alias(new_col)
    )


def clarify_strand(
    df: pl.DataFrame, bias_rate: float = 0.9, strandedness: str = "fr-firststrand"
):
    """Clarify strand assignments using F1R2/F2R1 bias when GTF annotation is
    ambiguous.

    Applies strand clarification to both the complete strand list (strand_all)
    and the majority strand (strand_majority), using both overall F1R2
    percentage (pct_f1r2) and alternate allele-specific F1R2 percentage
    (pct_f1r2_alt).

    This generates four new columns with clarified strand information, allowing
    comparison between different clarification strategies.

    Args:
        df (pl.DataFrame): Polars DataFrame with variant and strand information,
            including strand_all, strand_majority, pct_f1r2, and pct_f1r2_alt
            columns.
        bias_rate (float, optional): Threshold for considering strand bias
            significant. Default is 0.9, meaning ≥90% reads in one orientation
            is required.
        strandedness (str, optional): Library strandedness type. One of
            "fr-firststrand", "fr-secondstrand", or "unstranded". Default is
            "fr-firststrand".

    Returns:
        pl.DataFrame: DataFrame with four additional columns:
            - strand_all_clarified: Clarified using strand_all and pct_f1r2
            - strand_all_clarified_alt: Clarified using strand_all and pct_f1r2_alt
            - strand_majority_clarified: Clarified using strand_majority and pct_f1r2
            - strand_majority_clarified_alt: Clarified using strand_majority and pct_f1r2_alt
    """
    return df.with_columns(
        _clarify_strand(
            "strand_all", "strand_all_clarified", "pct_f1r2", bias_rate, strandedness
        ),
        _clarify_strand(
            "strand_all",
            "strand_all_clarified_alt",
            "pct_f1r2_alt",
            bias_rate,
            strandedness,
        ),
        _clarify_strand(
            "strand_majority",
            "strand_majority_clarified",
            "pct_f1r2",
            bias_rate,
            strandedness,
        ),
        _clarify_strand(
            "strand_majority",
            "strand_majority_clarified_alt",
            "pct_f1r2_alt",
            bias_rate,
            strandedness,
        ),
    )


def _detect_conflict(input_col, new_col, f1r2_pct_col, rate, strandedness):
    """Helper function to detect conflicts between GTF strand and F1R2/F2R1 bias.

    A conflict occurs when the strand-specific read orientation (F1R2/F2R1 bias)
    contradicts the GTF-assigned strand.

    For fr-firststrand (ISR):
    - pct_f1r2 >= rate AND strand is + → conflict (high F1R2 suggests - strand)
    - pct_f1r2 <= (1-rate) AND strand is - → conflict (low F1R2 suggests + strand)

    For fr-secondstrand (SR):
    - pct_f1r2 >= rate AND strand is - → conflict (high F1R2 suggests + strand)
    - pct_f1r2 <= (1-rate) AND strand is + → conflict (low F1R2 suggests - strand)

    For unstranded: No conflict detection (always False).

    Args:
        input_col (str): Name of input column with strand information (+ or -).
        new_col (str): Name of output column for conflict flag (boolean).
        f1r2_pct_col (str): Name of column with F1R2 percentage (0 to 1).
        rate (float): Threshold rate for conflict detection (typically 0.5 for
            majority-based conflict detection).
        strandedness (str): Library strandedness type.

    Returns:
        pl.Expr: Polars expression that evaluates to True when a conflict is detected.
    """
    mapping = _get_strand_mapping(strandedness)
    if mapping is None:
        return pl.lit(False).alias(new_col)

    high_strand, low_strand = mapping
    # Conflict: F1R2 bias says one strand, but GTF says the opposite
    return (
        pl.when(
            ((pl.col(f1r2_pct_col) >= rate) & (pl.col(input_col) == low_strand))
            | (
                (pl.col(f1r2_pct_col) <= (1 - rate))
                & (pl.col(input_col) == high_strand)
            )
        )
        .then(pl.lit(True))
        .otherwise(pl.lit(False))
        .alias(new_col)
    )


def detect_conflict(
    df: pl.DataFrame, bias_rate: float = 0.5, strandedness: str = "fr-firststrand"
):
    """Detect conflicts between GTF-assigned strand and F1R2/F2R1 bias.

    Compares the clarified strand assignments with the F1R2/F2R1 read
    orientation bias to identify variants where these two sources of strand
    information disagree.
    Creates four conflict flag columns corresponding to the four clarified
    strand columns.

    Args:
        df (pl.DataFrame): Polars DataFrame with variant and clarified strand
            information, including the four strand_*_clarified columns and
            pct_f1r2/pct_f1r2_alt.
        bias_rate (float, optional): Threshold for conflict detection. Default
            is 0.5, meaning any majority bias (>50%) in the opposite direction
            triggers a conflict.
        strandedness (str, optional): Library strandedness type. One of
            "fr-firststrand", "fr-secondstrand", or "unstranded". Default is
            "fr-firststrand".

    Returns:
        pl.DataFrame: DataFrame with four additional boolean columns:
            - conflict_all_clarified: Conflict for strand_all_clarified
            - conflict_all_clarified_alt: Conflict for strand_all_clarified_alt
            - conflict_majority_clarified: Conflict for strand_majority_clarified
            - conflict_majority_clarified_alt: Conflict for strand_majority_clarified_alt
    """
    return df.with_columns(
        _detect_conflict(
            "strand_all_clarified",
            "conflict_all_clarified",
            "pct_f1r2",
            bias_rate,
            strandedness,
        ),
        _detect_conflict(
            "strand_all_clarified_alt",
            "conflict_all_clarified_alt",
            "pct_f1r2_alt",
            bias_rate,
            strandedness,
        ),
        _detect_conflict(
            "strand_majority_clarified",
            "conflict_majority_clarified",
            "pct_f1r2",
            bias_rate,
            strandedness,
        ),
        _detect_conflict(
            "strand_majority_clarified_alt",
            "conflict_majority_clarified_alt",
            "pct_f1r2_alt",
            bias_rate,
            strandedness,
        ),
    )


