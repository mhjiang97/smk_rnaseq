def main():
    """Main function to assign strand information to RNA SNVs.

    Workflow:
    1. Parse command line arguments and configure logging
    2. Load VCF file for the specified sample
    3. Load GTF annotations (pickled PyRanges object)
    4. Extract F1R2/F2R1 counts from VCF and calculate strand bias metrics
    5. Extend transcript annotations by the specified amount to capture nearby
    variants
    6. Overlap variant positions with extended transcript features
    7. Assign strand based on GTF annotation (strand_all and strand_majority)
    8. Clarify ambiguous strand assignments using F1R2/F2R1 bias
    9. Detect conflicts between GTF and F1R2/F2R1-based strand assignments
    10. Write results to TSV file

    Input files:
    - VCF file: {caller}/{sample}/{sample}.snvs.vcf
    - GTF pickle: {gtf}.pkl

    Output file:
    - TSV file: {caller}/{sample}/{sample}.snvs.strand.tsv

    The output TSV contains:
    - Variant identifiers (chrom, pos, id, ref, alt)
    - F1R2/F2R1 counts and percentages
    - GTF-based strand assignments
    - Clarified strand assignments (4 versions)
    - Conflict flags (4 versions)
    """
    path_vcf = snakemake.input[0]  # type: ignore # noqa: F821
    path_pickle = snakemake.input[1]  # type: ignore # noqa: F821

    out_tsv = snakemake.output[0]  # type: ignore # noqa: F821

    rate_bias = snakemake.params[0]  # type: ignore # noqa: F821
    rate_conflict = snakemake.params[1]  # type: ignore # noqa: F821
    extend_transcript = snakemake.params[2]  # type: ignore # noqa: F821
    strandedness = snakemake.params[3]  # type: ignore # noqa: F821

    path_log = snakemake.log[0]  # type: ignore # noqa: F821

    sample = snakemake.wildcards.sample  # type: ignore # noqa: F821

    configure_logger(console_output=False, log_file=path_log, log_level="INFO")

    logging.info(f"Strandedness of sample '{sample}': {strandedness}")
    logging.info(
        f"Assigning strand information for sample '{sample}' using GTF pickle '{path_pickle}'."
    )

    with open(path_pickle, "rb") as f:
        gtf = pickle.load(f)

    prs_gtf: pr.PyRanges = gtf[gtf.Feature == "transcript"]
    prs_gtf_extended = prs_gtf.extend(extend_transcript)
    grs_gtf = prs2grs(prs_gtf_extended, drop_cols=["exon_number", "exon_id"])

    df = get_vcf_data(path_vcf, sample)

    grs_vcf = GenomicRanges.from_pandas(
        pl.DataFrame(
            {
                "seqnames": df["chrom"].to_list(),
                "starts": df["pos"].to_list(),
                "ends": df["pos"].to_list(),
            }
        ).to_pandas()
    )

    df_strand = get_strand(grs_vcf, grs_gtf)
    df = df.with_row_index().join(df_strand, on="index", how="left").drop("index")
    df = clarify_strand(df, bias_rate=rate_bias, strandedness=strandedness)
    df = detect_conflict(df, bias_rate=rate_conflict, strandedness=strandedness)

    df.write_csv(out_tsv, separator="\t", include_header=True)


# %%
if __name__ == "__main__":
    main()


