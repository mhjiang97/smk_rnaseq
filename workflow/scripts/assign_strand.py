def get_strand(vcf_grs: GenomicRanges, gtf_grs: GenomicRanges):
    """Determine strand information for variants by overlapping with GTF
    annotations.

    For each variant position, finds overlapping transcript features from the
    GTF and extracts their strand annotations. When a variant overlaps multiple
    transcripts, both the complete set of strands and the majority (mode) strand
    are reported. Variants with no transcript overlap are assigned "." for both
    strand columns.

    Strand assignment scenarios:
    - Single transcript overlap: Unambiguous strand assignment (+ or -)
    - Multiple transcripts, same strand: Unambiguous strand assignment
    - Multiple transcripts, different strands: Ambiguous (e.g., "+,-" or ".,+")
    - No transcript overlap: Missing strand (".")

    Args:
        vcf_grs (GenomicRanges): GenomicRanges object containing variant
            positions.
        gtf_grs (GenomicRanges): GenomicRanges object containing GTF transcript
            features with strand annotations.

    Returns:
        pl.DataFrame: Polars DataFrame with columns:
            - index: Row index from original variant data
            - strand_all: Comma-separated list of all unique strands (sorted),
              "." for variants with no transcript overlap
            - strand_majority: Comma-separated list of most frequent strand(s)
              (sorted), "." for variants with no transcript overlap
    """
    all_indices = pl.DataFrame(
        {"index": pl.Series(range(len(vcf_grs)), dtype=pl.UInt32)}
    )

    hits = gtf_grs.find_overlaps(vcf_grs, ignore_strand=True)
    hit_query = hits["query_hits"]
    hit_self = hits["self_hits"]
    strands_gtf = gtf_grs.get_strand(as_type="list")

    df_hits = pl.DataFrame(
        {
            "index": hit_query,
            "strand": [strands_gtf[i] for i in hit_self],  # type: ignore
        }
    )

    df_strand = (
        df_hits.group_by("index")
        .agg(
            pl.col("strand").unique().alias("strand_list"),
            pl.col("strand").mode().alias("strand_mode_list"),
        )
        .with_columns(
            pl.col("strand_list")
            .map_elements(lambda lst: ",".join(sorted(lst)), return_dtype=pl.Utf8)
            .alias("strand_all"),
            pl.col("strand_mode_list")
            .map_elements(lambda lst: ",".join(sorted(lst)), return_dtype=pl.Utf8)
            .alias("strand_majority"),
        )
        .select(["index", "strand_all", "strand_majority"])
    )

    return all_indices.join(df_strand, on="index", how="left").with_columns(
        pl.col("strand_all").fill_null("."),
        pl.col("strand_majority").fill_null("."),
    )


