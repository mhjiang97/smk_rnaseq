def prs2grs(prs, drop_cols=None):
    """Convert PyRanges object to GenomicRanges object with coordinate system conversion.

    PyRanges uses 0-based half-open intervals [start, end), while GenomicRanges
    uses 1-based closed intervals [start, end]. This function performs the necessary
    conversion by adding 1 to all start positions.

    Column name mappings:
    - Chromosome -> seqnames
    - Start -> starts (with +1 adjustment)
    - End -> ends
    - Strand -> strand

    Args:
        prs (pr.PyRanges): PyRanges object to convert.
        drop_cols (list, optional): List of column names to drop from the result.
            Useful for removing PyRanges-specific columns that aren't needed.

    Returns:
        GenomicRanges: GenomicRanges object with 1-based coordinate system.
    """
    tmp: pd.DataFrame = prs.as_df().rename(
        columns={
            "Chromosome": "seqnames",
            "Start": "starts",
            "End": "ends",
            "Strand": "strand",
        }
    )
    if drop_cols is not None:
        tmp = tmp.drop(columns=drop_cols)
    tmp["starts"] = tmp["starts"] + 1

    return GenomicRanges.from_pandas(tmp)


